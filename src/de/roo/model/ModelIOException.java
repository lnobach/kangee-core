package de.roo.model;

/**
 * 
 * @author Leo Nobach
 *
 */
public class ModelIOException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4009443302632127259L;

	public ModelIOException() {
		super();
	}

	public ModelIOException(String message, Throwable cause) {
		super(message, cause);
	}

	public ModelIOException(String message) {
		super(message);
	}

	public ModelIOException(Throwable cause) {
		super(cause);
	}

}
